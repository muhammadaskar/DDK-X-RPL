package VariabelTipeData;

public class Variabel {
    public static void main(String[] args) {
        String nama = "Harry";          // inisialisasi nilai untuk variabel nama dengan tipe data string
        int umur = 15;                  // inisialisasi nilai untuk variabel umur dengan tipe data int
        double beratBadan = 45.3;       // inisialisasi nilai untuk variabel beratBdan dengan tipe data double
        char jenisKelamin = 'L';        // inisialisasi nilai untuk variabel jenisKelamin dengan tipe data char

        System.out.println("Nama : " +nama);
        System.out.println("Umur : " +umur);
        System.out.println("Berat Badan : " +beratBadan);
        System.out.println("Jenis Kelamin : " +jenisKelamin);


    }
}
